package day03;

public class Ex01 {
	public static void main(String[] args) {
		
		int n = 1;
		
		switch(n) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
		case 3:
			System.out.println("3");
		case 'A':
			System.out.println("A");
		default:
			System.out.println("매치 되는 것 없음");
		}
	}
}




